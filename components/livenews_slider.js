  i
  
  var itemNumber = newsLists[i].querySelectorAll("img").length;
  var clickCounter = 0;
  if (window.length > 400) {
    
  }
  elem.addEventListener("click", function () {

    const ratio = Math.ceil(window.innerWidth / 220);
    clickCounter++;

    if (itemNumber - (5 + clickCounter)+(5-ratio) >= 0) {
      newsLists[i].style.transform = `translateX(${
        newsLists[i].computedStyleMap().get("transform")[0].x.value -260
      }px)`;
    } else {
      newsLists[i].style.transform = "translateX(0)";
      clickCounter = 0;
    }
  
 

  });

  
